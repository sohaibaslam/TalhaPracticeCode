class Printer:
   
    def print_max_temp(self,file,max_temp):
        print(f"Maximum Temperature for {file} is {max_temp}")
        
    def print_min_temp(self,file,min_temp):
        print(f"Minimum Temperature for {file} is {min_temp}")
        
    def print_max_humidity(self,file,max_humidity):
        print(f"Maximum Humidity for {file} is {max_humidity}")
        
    def print_min_humidity(self,file,min_humidity):
        print(f"Minimum Humidity for {file} is {min_humidity}")
        
    def print_highest_ave_temp(self,file,max_avg_temp):
        print(f"Highest Average Temperature for {file} is {max_avg_temp}")
        
    def print_lowest_ave_temp(self,file,min_avg_temp):
        print(f"Lowest Average Temperature for {file} is {min_avg_temp}")
    
